        if(cn.equals("")){
            JOptionPane.showMessageDialog(rootPane,"Enter CNIC");
        }else{
            try{
                Statement pSt= conn.createStatement();
                String delete= "delete from voter where cnic='"+cn+"'";
                int de= pSt.executeUpdate(delete);
                if(de!=0){
                    JOptionPane.showMessageDialog(rootPane,"Deleted");
                }else{
                    JOptionPane.showMessageDialog(rootPane,"Error 404.");
                }
            }catch(Exception e){
                
            }
        }
  
